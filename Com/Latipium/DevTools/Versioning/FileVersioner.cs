using System;
using System.IO;
using System.Text.RegularExpressions;

namespace Com.Latipium.DevTools.Versioning {
    public class FileVersioner {
        public readonly string Filename;
        public readonly Version Version;

        public bool Exists {
            get {
                return File.Exists(Filename);
            }
        }

        public void ReplaceAll() {
            string contents;
            using (TextReader reader = new StreamReader(Filename)) {
                contents = reader.ReadToEnd();
            }
            contents = Regex.Replace(contents, "([^A-Za-z0-9])AssemblyVersion([ \t]*)\\(([ \t]*)\"[^\"]*\"([ \t]*)\\)", string.Format("$1AssemblyVersion$2($3\"{0}\"$4)", Version));
            using (TextWriter writer = new StreamWriter(Filename)) {
                writer.Write(contents);
            }
        }

        public void Create() {
            using (TextWriter writer = new StreamWriter(Filename)) {
                writer.WriteLine("// This file was auto-generated by the Latipium Development Tools");
                writer.WriteLine("// Make sure this is in your .gitignore file!");
                writer.WriteLine("using System.Reflection;");
                writer.WriteLine();
                writer.WriteLine("[assembly: AssemblyVersion(\"{0}\")]", Version);
            }
        }

        public FileVersioner(string filename, Version version) {
            Filename = filename;
            Version = version;
        }
    }
}

